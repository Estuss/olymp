// =========================== 1. ПРЕФИКС-ФУНКЦИЯ ====================================
// НАЗНАЧЕНИЕ: вычисление для каждой позиции i длины наибольшего собственного
//            префикса строки, который одновременно является суффиксом
//            подстроки s[0..i]
// СЛОЖНОСТЬ: время O(N), память O(N)
// ==================================================================================

/**
 * @brief Вычисление префикс-функции для строки
 * 
 * @param s Исходная строка (строка символов)
 * @return vector<int> Массив pi, где pi[i] - длина наибольшего собственного
 *                     префикса подстроки s[0..i], который является её суффиксом
 * 
 * @details Префикс-функция определяется следующим образом:
 *          pi[i] = max{k | k < i+1 и s[0..k-1] == s[i-k+1..i]}
 *          
 *          Алгоритм работает за O(N) благодаря использованию уже вычисленных
 *          значений. На каждом шаге пытаемся расширить текущий префикс,
 *          а если не получается - переходим по pi[k-1].
 *          
 *          Пример для строки "abacaba":
 *          i:   0 1 2 3 4 5 6
 *          s:   a b a c a b a
 *          pi:  0 0 1 0 1 2 3
 *          
 *          Применение:
 *          - Поиск подстроки в строке (алгоритм КМП)
 *          - Поиск всех вхождений подстроки
 *          - Вычисление периода строки
 *          - Построение автомата КМП
 */
vector<int> prefixFunction(const string& s) {
    int n = s.length();
    vector<int> pi(n, 0);
    
    // pi[0] всегда 0, так как у односимвольной строки нет собственных префиксов
    for (int i = 1; i < n; i++) {
        // j - длина текущего совпадающего префикса для позиции i-1
        int j = pi[i - 1];
        
        // Пытаемся расширить префикс: проверяем, совпадает ли следующий символ
        // Если не совпадает, переходим к меньшему префиксу-кандидату
        while (j > 0 && s[i] != s[j]) {
            j = pi[j - 1];  // Ключевая оптимизация КМП - используем уже вычисленные pi
        }
        
        // Если символы совпали, увеличиваем длину префикса
        if (s[i] == s[j]) {
            j++;
        }
        
        pi[i] = j;
    }
    
    return pi;
}

/**
 * @brief Ускоренная версия вычисления префикс-функции с оптимизацией переходов
 * 
 * @param s Исходная строка
 * @return vector<int> Массив pi
 * 
 * @details Эта версия дополнительно оптимизирует переходы, запоминая
 *          "автоматные" переходы для каждого состояния. Полезна при
 *          многократном поиске в одной и той же строке.
 */
vector<int> prefixFunctionOptimized(const string& s) {
    int n = s.length();
    vector<int> pi(n, 0);
    
    for (int i = 1; i < n; i++) {
        int j = pi[i - 1];
        while (j > 0 && s[i] != s[j]) {
            j = pi[j - 1];
        }
        if (s[i] == s[j]) {
            j++;
        }
        pi[i] = j;
        
        // Оптимизация: если следующий символ совпадает, сразу проставляем pi
        if (i + 1 < n && s[i + 1] == s[j]) {
            pi[i] = pi[j - 1];
        }
    }
    
    return pi;
}

/**
 * @brief Вычисление периода строки с помощью префикс-функции
 * 
 * @param s Исходная строка
 * @return int Длина минимального периода строки
 * 
 * @details Строка имеет период длины k, если s[i] == s[i+k] для всех i.
 *          Используя префикс-функцию, период вычисляется по формуле:
 *          period = n - pi[n-1], если n % period == 0
 *          
 *          Пример: "abcabcabc" -> pi[8]=6, period=9-6=3
 */
int findPeriod(const string& s) {
    int n = s.length();
    vector<int> pi = prefixFunction(s);
    int period = n - pi[n - 1];
    
    // Проверяем, действительно ли это период (должен делить длину нацело)
    if (n % period == 0) {
        return period;
    }
    return n;  // Строка непериодическая
}

/**
 * @brief Вычисление всех бордюров (границ) строки
 * 
 * @param s Исходная строка
 * @return vector<int> Длины всех бордюров строки в порядке убывания
 * 
 * @details Бордюр (граница) - это подстрока, которая одновременно является
 *          и префиксом, и суффиксом строки. Все бордюры получаются
 *          рекурсивным применением pi[n-1], pi[pi[n-1]-1], ...
 */
vector<int> getAllBorders(const string& s) {
    int n = s.length();
    vector<int> pi = prefixFunction(s);
    vector<int> borders;
    
    int k = pi[n - 1];
    while (k > 0) {
        borders.push_back(k);
        k = pi[k - 1];
    }
    
    return borders;
}

// =========================== 2. Z-ФУНКЦИЯ =========================================
// НАЗНАЧЕНИЕ: вычисление для каждой позиции i длины наибольшего общего
//            префикса всей строки и её суффикса, начинающегося с позиции i
// СЛОЖНОСТЬ: время O(N), память O(N)
// ==================================================================================

/**
 * @brief Вычисление Z-функции для строки
 * 
 * @param s Исходная строка
 * @return vector<int> Массив z, где z[i] - длина наибольшего общего префикса
 *                     строки s и подстроки s[i..n-1]
 * 
 * @details Z-функция определяется как:
 *          z[0] = 0 (или n, по соглашению)
 *          z[i] = max{k | s[0..k-1] == s[i..i+k-1]}
 *          
 *          Алгоритм поддерживает отрезок [l, r] с максимальным r,
 *          для которого s[l..r] является префиксом строки.
 *          Это позволяет вычислять z[i] за O(1) в среднем.
 *          
 *          Пример для строки "abacaba":
 *          i:   0 1 2 3 4 5 6
 *          s:   a b a c a b a
 *          z:   0 0 1 0 3 0 1
 *          
 *          Применение:
 *          - Поиск подстроки (через строку pattern + '#' + text)
 *          - Поиск периода строки
 *          - Сжатие строки (поиск повторяющихся фрагментов)
 */
vector<int> zFunction(const string& s) {
    int n = s.length();
    vector<int> z(n, 0);
    
    // Границы самого правого найденного отрезка, совпадающего с префиксом
    int l = 0;  // Левая граница отрезка
    int r = 0;  // Правая граница отрезка
    
    // z[0] обычно полагают равным 0 (или n, по соглашению)
    for (int i = 1; i < n; i++) {
        if (i <= r) {
            // Если i внутри отрезка [l, r], можем использовать уже вычисленные значения
            // z[i] соответствует z[i-l], но ограничено длиной оставшейся части отрезка
            z[i] = min(r - i + 1, z[i - l]);
        }
        
        // Наивно расширяем совпадение, пока символы равны
        while (i + z[i] < n && s[z[i]] == s[i + z[i]]) {
            z[i]++;
        }
        
        // Обновляем границы самого правого отрезка
        if (i + z[i] - 1 > r) {
            l = i;
            r = i + z[i] - 1;
        }
    }
    
    // По соглашению z[0] часто полагают равным длине строки
    // z[0] = n;  // Раскомментировать, если нужно
    
    return z;
}

/**
 * @brief Поиск всех вхождений подстроки в строку с помощью Z-функции
 * 
 * @param text Текст, в котором ищем
 * @param pattern Искомая подстрока (шаблон)
 * @return vector<int> Индексы всех вхождений pattern в text
 * 
 * @details Формируем строку pattern + '#' + text и вычисляем Z-функцию.
 *          Вхождения pattern соответствуют позициям, где z[i] == len(pattern).
 *          Символ '#' (или любой символ не из алфавита) нужен, чтобы
 *          избежать ложных срабатываний на границе pattern и text.
 */
vector<int> findOccurrencesZ(const string& text, const string& pattern) {
    string combined = pattern + "#" + text;
    vector<int> z = zFunction(combined);
    vector<int> occurrences;
    
    int patternLen = pattern.length();
    for (int i = patternLen + 1; i < (int)combined.length(); i++) {
        if (z[i] == patternLen) {
            occurrences.push_back(i - patternLen - 1);
        }
    }
    
    return occurrences;
}

/**
 * @brief Поиск периода строки с помощью Z-функции
 * 
 * @param s Исходная строка
 * @return int Длина минимального периода
 * 
 * @details Период существует, если z[i] == n - i для некоторого i.
 *          Первое такое i и будет длиной периода.
 */
int findPeriodZ(const string& s) {
    int n = s.length();
    vector<int> z = zFunction(s);
    
    for (int i = 1; i < n; i++) {
        if (z[i] == n - i && n % i == 0) {
            return i;
        }
    }
    return n;  // Период не найден
}

/**
 * @brief Проверка, является ли строка степенью другой строки
 * 
 * @param s Исходная строка
 * @return pair<bool, int> Пара: (true, k) если s = t^k, иначе (false, 0)
 * 
 * @details Строка является степенью, если её длина делится на период.
 *          Например, "abcabcabc" = "abc"^3
 */
pair<bool, int> isPowerOfString(const string& s) {
    int period = findPeriodZ(s);
    if (period < (int)s.length() && s.length() % period == 0) {
        return {true, s.length() / period};
    }
    return {false, 0};
}

// =========================== 3. АЛГОРИТМ КНУТА-МОРРИСА-ПРАТТА (КМП) =================
// НАЗНАЧЕНИЕ: эффективный поиск подстроки в строке за O(N+M)
// СЛОЖНОСТЬ: время O(N+M), память O(M), где N - длина текста, M - длина шаблона
// ==================================================================================

/**
 * @brief Поиск всех вхождений подстроки в строку с помощью алгоритма КМП
 * 
 * @param text Текст, в котором производится поиск
 * @param pattern Искомая подстрока (шаблон)
 * @return vector<int> Индексы всех вхождений pattern в text (0-indexed)
 * 
 * @details Алгоритм КМП использует префикс-функцию шаблона для того,
 *          чтобы избежать откатов в тексте. При несовпадении мы сдвигаем
 *          шаблон не на 1, а на величину, определяемую префикс-функцией.
 *          
 *          Принцип работы:
 *          1. Вычисляем префикс-функцию для шаблона
 *          2. Проходим по тексту, поддерживая длину совпавшего префикса
 *          3. При несовпадении используем pi для "перемотки" шаблона
 *          4. Когда длина совпадения равна длине шаблона - нашли вхождение
 */
vector<int> KMP(const string& text, const string& pattern) {
    int n = text.length();
    int m = pattern.length();
    
    // Предварительно вычисляем префикс-функцию для шаблона
    vector<int> pi = prefixFunction(pattern);
    vector<int> occurrences;
    
    int j = 0;  // Длина текущего совпавшего префикса шаблона
    
    for (int i = 0; i < n; i++) {
        // Пока нет совпадения и j > 0, откатываемся по префикс-функции
        while (j > 0 && text[i] != pattern[j]) {
            j = pi[j - 1];
        }
        
        // Если символы совпали, увеличиваем длину совпадения
        if (text[i] == pattern[j]) {
            j++;
        }
        
        // Если совпал весь шаблон
        if (j == m) {
            occurrences.push_back(i - m + 1);  // Индекс начала вхождения
            j = pi[j - 1];  // Продолжаем поиск следующих вхождений
        }
    }
    
    return occurrences;
}

/**
 * @brief Расширенный алгоритм КМП с подсчётом статистики
 * 
 * @param text Текст для поиска
 * @param pattern Шаблон
 * @return tuple<vector<int>, int, int> Кортеж: (вхождения, кол-во сравнений, кол-во переходов)
 * 
 * @details Полезно для анализа эффективности алгоритма и отладки
 */
tuple<vector<int>, int, int> KMPWithStats(const string& text, const string& pattern) {
    int n = text.length();
    int m = pattern.length();
    vector<int> pi = prefixFunction(pattern);
    vector<int> occurrences;
    
    int comparisons = 0;  // Счётчик сравнений символов
    int jumps = 0;        // Счётчик переходов по префикс-функции
    
    int j = 0;
    for (int i = 0; i < n; i++) {
        while (j > 0 && text[i] != pattern[j]) {
            comparisons++;
            jumps++;
            j = pi[j - 1];
        }
        comparisons++;  // Сравнение после while или начальное
        
        if (text[i] == pattern[j]) {
            j++;
        }
        
        if (j == m) {
            occurrences.push_back(i - m + 1);
            jumps++;
            j = pi[j - 1];
        }
    }
    
    return {occurrences, comparisons, jumps};
}

/**
 * @brief Построение автомата КМП для многократного поиска
 * 
 * @param pattern Шаблон, для которого строится автомат
 * @return vector<vector<int>> Таблица переходов: automaton[state][char] = next_state
 * 
 * @details Автомат КМП позволяет искать шаблон в разных текстах без
 *          повторного вычисления префикс-функции. Полезен, когда нужно
 *          найти один шаблон во многих текстах.
 *          
 *          Состояния: 0..m (m - конечное состояние, вхождение найдено)
 *          Переходы: для каждого состояния и символа алфавита
 */
vector<vector<int>> buildKMPAutomaton(const string& pattern, int alphabetSize = 26) {
    int m = pattern.length();
    vector<int> pi = prefixFunction(pattern);
    
    // automaton[state][c] = следующее состояние при чтении символа c
    vector<vector<int>> automaton(m + 1, vector<int>(alphabetSize, 0));
    
    for (int state = 0; state <= m; state++) {
        for (int c = 0; c < alphabetSize; c++) {
            char ch = 'a' + c;
            
            if (state < m && ch == pattern[state]) {
                automaton[state][c] = state + 1;
            } else if (state > 0) {
                automaton[state][c] = automaton[pi[state - 1]][c];
            } else {
                automaton[state][c] = 0;
            }
        }
    }
    
    return automaton;
}

/**
 * @brief Поиск с использованием автомата КМП
 * 
 * @param text Текст для поиска
 * @param automaton Автомат, построенный для шаблона
 * @param patternLen Длина шаблона (количество состояний - 1)
 * @return vector<int> Индексы всех вхождений
 */
vector<int> searchWithAutomaton(const string& text, 
                                 const vector<vector<int>>& automaton, 
                                 int patternLen) {
    vector<int> occurrences;
    int state = 0;
    
    for (int i = 0; i < (int)text.length(); i++) {
        int c = text[i] - 'a';  // Предполагаем строчные латинские буквы
        state = automaton[state][c];
        
        if (state == patternLen) {
            occurrences.push_back(i - patternLen + 1);
        }
    }
    
    return occurrences;
}

/**
 * @brief Поиск всех вхождений с учётом перекрытий и без
 * 
 * @param text Текст
 * @param pattern Шаблон
 * @param allowOverlap Разрешить перекрывающиеся вхождения
 * @return vector<int> Индексы вхождений
 * 
 * @details По умолчанию КМП находит все вхождения, включая перекрывающиеся.
 *          Если allowOverlap = false, после нахождения вхождения поиск
 *          продолжается с позиции после конца найденного шаблона.
 */
vector<int> KMPWithOverlapControl(const string& text, const string& pattern, 
                                   bool allowOverlap = true) {
    int n = text.length();
    int m = pattern.length();
    vector<int> pi = prefixFunction(pattern);
    vector<int> occurrences;
    
    int j = 0;
    for (int i = 0; i < n; i++) {
        while (j > 0 && text[i] != pattern[j]) {
            j = pi[j - 1];
        }
        
        if (text[i] == pattern[j]) {
            j++;
        }
        
        if (j == m) {
            occurrences.push_back(i - m + 1);
            if (allowOverlap) {
                j = pi[j - 1];  // Продолжаем с учётом возможных перекрытий
            } else {
                j = 0;  // Начинаем поиск заново после текущего вхождения
            }
        }
    }
    
    return occurrences;
}

// =========================== ДОПОЛНИТЕЛЬНЫЕ ФУНКЦИИ ================================

/**
 * @brief Поиск подстроки с одним несовпадением (приближённый поиск)
 * 
 * @param text Текст
 * @param pattern Шаблон
 * @return vector<int> Позиции, где шаблон совпадает с точностью до 1 ошибки
 * 
 * @details Использует Z-функцию для прямого и обратного совпадения.
 *          Сложность: O(N + M)
 */
vector<int> findWithOneMismatch(const string& text, const string& pattern) {
    int n = text.length();
    int m = pattern.length();
    
    if (m > n) return {};
    
    // Вычисляем Z-функцию для прямого сравнения: pattern + '#' + text
    vector<int> z_forward = zFunction(pattern + "#" + text);
    
    // Вычисляем Z-функцию для обратного сравнения
    string rev_pattern = pattern;
    string rev_text = text;
    reverse(rev_pattern.begin(), rev_pattern.end());
    reverse(rev_text.begin(), rev_text.end());
    vector<int> z_backward = zFunction(rev_pattern + "#" + rev_text);
    
    vector<int> occurrences;
    
    for (int i = 0; i <= n - m; i++) {
        // Длина совпадения с начала шаблона
        int forward_match = z_forward[m + 1 + i];
        
        if (forward_match >= m) {
            // Полное совпадение без ошибок
            occurrences.push_back(i);
            continue;
        }
        
        // Длина совпадения с конца шаблона
        int backward_match = z_backward[m + 1 + (n - i - m)];
        
        // Проверяем, перекрывают ли совпадения всё, кроме одного символа
        if (forward_match + backward_match >= m - 1) {
            occurrences.push_back(i);
        }
    }
    
    return occurrences;
}

/**
 * @brief Сравнение эффективности разных алгоритмов поиска подстроки
 * 
 * @param text Текст для поиска
 * @param pattern Шаблон
 */
void compareSearchAlgorithms(const string& text, const string& pattern) {
    cout << "\n========== СРАВНЕНИЕ АЛГОРИТМОВ ПОИСКА ПОДСТРОКИ ==========\n";
    cout << "Текст: \"" << text << "\"\n";
    cout << "Шаблон: \"" << pattern << "\"\n\n";
    
    // КМП
    auto start = chrono::high_resolution_clock::now();
    vector<int> kmp_result = KMP(text, pattern);
    auto end = chrono::high_resolution_clock::now();
    auto kmp_time = chrono::duration_cast<chrono::microseconds>(end - start);
    
    // Z-функция
    start = chrono::high_resolution_clock::now();
    vector<int> z_result = findOccurrencesZ(text, pattern);
    end = chrono::high_resolution_clock::now();
    auto z_time = chrono::duration_cast<chrono::microseconds>(end - start);
    
    // Наивный поиск (для сравнения)
    start = chrono::high_resolution_clock::now();
    vector<int> naive_result;
    for (int i = 0; i <= (int)text.length() - (int)pattern.length(); i++) {
        if (text.substr(i, pattern.length()) == pattern) {
            naive_result.push_back(i);
        }
    }
    end = chrono::high_resolution_clock::now();
    auto naive_time = chrono::duration_cast<chrono::microseconds>(end - start);
    
    cout << "Результаты:\n";
    cout << "1. КМП:               " << kmp_result.size() << " вхождений, время: " 
         << kmp_time.count() << " мкс\n";
    cout << "   Позиции: ";
    for (int pos : kmp_result) cout << pos << " ";
    cout << "\n";
    
    cout << "2. Z-функция:         " << z_result.size() << " вхождений, время: " 
         << z_time.count() << " мкс\n";
    cout << "   Позиции: ";
    for (int pos : z_result) cout << pos << " ";
    cout << "\n";
    
    cout << "3. Наивный алгоритм:  " << naive_result.size() << " вхождений, время: " 
         << naive_time.count() << " мкс\n";
    
    cout << "\nТеоретическая сложность:\n";
    cout << "- КМП: O(|text| + |pattern|) = O(" << text.length() << " + " 
         << pattern.length() << ") = O(" << text.length() + pattern.length() << ")\n";
    cout << "- Z-функция: O(|text| + |pattern|) = O(" << text.length() + pattern.length() << ")\n";
    cout << "- Наивный: O(|text| * |pattern|) = O(" << text.length() * pattern.length() << ")\n";
}

