#include <bits/stdc++.h>
using namespace std;

// =========================== 1. БИНАРНЫЙ ПОИСК ====================================
// НАЗНАЧЕНИЕ: поиск элемента в отсортированном массиве или бинарный поиск по ответу
// СЛОЖНОСТЬ: время O(log N), память O(1)
// ==================================================================================

/**
 * @brief Бинарный поиск в отсортированном массиве
 * 
 * @param arr Отсортированный вектор целых чисел (по возрастанию)
 * @param target Искомое значение
 * @return int Индекс первого вхождения target или -1, если элемент не найден
 * 
 */
int binarySearch(const vector<int>& arr, int target) {
    int left = 0;                    // Левая граница поиска (включительно)
    int right = arr.size() - 1;      // Правая граница поиска (включительно)
    
    while (left <= right) {
        // Предотвращаем переполнение при вычислении среднего индекса
        int mid = left + (right - left) / 2;
        
        if (arr[mid] == target) {
            return mid;              // Элемент найден, возвращаем его индекс
        } else if (arr[mid] < target) {
            left = mid + 1;          // Ищем в правой половине
        } else {
            right = mid - 1;         // Ищем в левой половине
        }
    }
    return -1;                       // Элемент не найден
}

/**
 * @brief Бинарный поиск первого элемента >= target (lower_bound)
 * 
 * @param arr Отсортированный вектор целых чисел
 * @param target Пороговое значение
 * @return int Индекс первого элемента >= target или arr.size(), если все элементы меньше
 * 
 * @details Используется для поиска позиции вставки и решения задач типа
 *          "найти минимальное значение, удовлетворяющее условию"
 */
int lowerBound(const vector<int>& arr, int target) {
    int left = 0;
    int right = arr.size();          // right указывает за последний элемент
    
    while (left < right) {
        int mid = left + (right - left) / 2;
        if (arr[mid] < target) {
            left = mid + 1;          // Все элементы слева от mid строго меньше target
        } else {
            right = mid;             // arr[mid] >= target, сужаем правую границу
        }
    }
    return left;                     // Первый индекс, где arr[i] >= target
}

// =========================== 2. ПРЕФИКСНЫЕ СУММЫ ==================================
// НАЗНАЧЕНИЕ: вычисление суммы на любом подотрезке массива за O(1)
// СЛОЖНОСТЬ: предобработка O(N), запрос O(1), память O(N)
// ==================================================================================

/**
 * @brief Построение массива префиксных сумм
 * 
 * @param a Исходный массив целых чисел
 * @return vector<long long> Массив pref, где pref[i] = сумма первых i элементов
 * 
 */
vector<long long> buildPrefixSums(const vector<int>& a) {
    int n = a.size();
    vector<long long> pref(n + 1, 0);  // pref[0] = 0 для удобства
    
    for (int i = 0; i < n; i++) {
        pref[i + 1] = pref[i] + a[i];  // Накапливаем сумму
    }
    return pref;
}

/**
 * @brief Вычисление суммы на отрезке [L, R] с использованием префиксных сумм
 * 
 * @param pref Массив префиксных сумм, построенный функцией buildPrefixSums
 * @param L Левая граница отрезка (включительно, 0-indexed)
 * @param R Правая граница отрезка (включительно, 0-indexed)
 * @return long long Сумма элементов от L до R включительно
 * 
 */
long long rangeSum(const vector<long long>& pref, int L, int R) {
    return pref[R + 1] - pref[L];
}

// =========================== 3. МЕТОД ДВУХ УКАЗАТЕЛЕЙ ==============================
// НАЗНАЧЕНИЕ: решение задач на подотрезки/подпоследовательности за линейное время
// СЛОЖНОСТЬ: время O(N), память O(1)
// ==================================================================================

/**
 * @brief Поиск пары элементов с заданной суммой в отсортированном массиве
 * 
 * @param arr Отсортированный по возрастанию массив целых чисел
 * @param target Целевая сумма
 * @return pair<int, int> Индексы пары элементов или {-1, -1}, если пара не найдена
 * 
 * @details Алгоритм использует два указателя: left в начале, right в конце.
 *          На каждом шаге вычисляется сумма arr[left] + arr[right]:
 *          - Если сумма == target -> решение найдено
 *          - Если сумма < target -> нужно увеличить сумму (left++)
 *          - Если сумма > target -> нужно уменьшить сумму (right--)
 */
pair<int, int> twoSumSorted(const vector<int>& arr, int target) {
    int left = 0;                     // Левый указатель (минимальный элемент)
    int right = arr.size() - 1;       // Правый указатель (максимальный элемент)
    
    while (left < right) {
        long long sum = (long long)arr[left] + arr[right];  // Предотвращаем переполнение
        
        if (sum == target) {
            return {left, right};     // Пара найдена
        } else if (sum < target) {
            left++;                   // Увеличиваем сумму, двигая левый указатель
        } else {
            right--;                  // Уменьшаем сумму, двигая правый указатель
        }
    }
    return {-1, -1};                  // Пара не найдена
}

/**
 * @brief Поиск длины максимального подотрезка с суммой <= limit
 * 
 * @param arr Массив неотрицательных целых чисел
 * @param limit Максимально допустимая сумма
 * @return int Длина самого длинного подотрезка с суммой <= limit
 * 
 * @details Используется скользящее окно (разновидность двух указателей).
 *          right указатель расширяет окно, left - сужает, когда сумма превышает лимит.
 */
int maxSubarrayLengthWithSumLimit(const vector<int>& arr, int limit) {
    int n = arr.size();
    int left = 0;                     // Левая граница окна
    long long currentSum = 0;         // Текущая сумма в окне
    int maxLength = 0;                // Максимальная длина подходящего окна
    
    for (int right = 0; right < n; right++) {
        currentSum += arr[right];     // Расширяем окно вправо
        
        // Если сумма превысила лимит, сдвигаем левую границу
        while (currentSum > limit && left <= right) {
            currentSum -= arr[left];
            left++;
        }
        
        // Обновляем максимальную длину
        maxLength = max(maxLength, right - left + 1);
    }
    return maxLength;
}

// =========================== 4. ДЕРЕВО ОТРЕЗКОВ ===================================
// НАЗНАЧЕНИЕ: запросы на отрезках (сумма, min, max) и обновления за O(log N)
// СЛОЖНОСТЬ: построение O(N), запрос O(log N), память O(4N)
// ==================================================================================


/**
 * @class SegmentTreeMinMax
 * @brief Дерево отрезков с поддержкой операций минимума, максимума и суммы
 * 
 * @details Расширенная версия дерева отрезков, которая хранит в каждой вершине
 *          сразу три значения: сумму, минимум и максимум на соответствующем отрезке.
 *          Это позволяет отвечать на разнообразные запросы за O(log N).
 */
class SegmentTreeMinMax {
private:
    int n;                                   // Размер исходного массива
    vector<long long> sumTree;               // Дерево для сумм
    vector<int> minTree;                     // Дерево для минимумов
    vector<int> maxTree;                     // Дерево для максимумов
    
    /**
     * @brief Рекурсивное построение всех трёх деревьев
     * 
     * @param a Исходный массив
     * @param v Номер текущей вершины
     * @param tl Левая граница отрезка вершины v
     * @param tr Правая граница отрезка вершины v
     */
    void build(const vector<int>& a, int v, int tl, int tr) {
        if (tl == tr) {
            // Лист дерева - хранит один элемент
            sumTree[v] = a[tl];
            minTree[v] = a[tl];
            maxTree[v] = a[tl];
        } else {
            int tm = (tl + tr) / 2;
            // Рекурсивно строим левое и правое поддеревья
            build(a, v * 2, tl, tm);
            build(a, v * 2 + 1, tm + 1, tr);
            // Комбинируем значения из детей
            sumTree[v] = sumTree[v * 2] + sumTree[v * 2 + 1];
            minTree[v] = min(minTree[v * 2], minTree[v * 2 + 1]);
            maxTree[v] = max(maxTree[v * 2], maxTree[v * 2 + 1]);
        }
    }
    
    /**
     * @brief Запрос суммы на отрезке [l, r]
     */
    long long querySum(int v, int tl, int tr, int l, int r) {
        if (l > r) {
            return 0;
        }
        if (l == tl && r == tr) {
            return sumTree[v];
        }
        int tm = (tl + tr) / 2;
        return querySum(v * 2, tl, tm, l, min(r, tm)) +
               querySum(v * 2 + 1, tm + 1, tr, max(l, tm + 1), r);
    }
    
    /**
     * @brief Запрос минимума на отрезке [l, r]
     * 
     * @param v Номер текущей вершины
     * @param tl Левая граница отрезка вершины v
     * @param tr Правая граница отрезка вершины v
     * @param l Левая граница запроса
     * @param r Правая граница запроса
     * @return int Минимальное значение на отрезке [l, r]
     */
    int queryMin(int v, int tl, int tr, int l, int r) {
        if (l > r) {
            return INT_MAX;                  // Для пустого запроса возвращаем "бесконечность"
        }
        if (l == tl && r == tr) {
            return minTree[v];               // Отрезок полностью совпадает с запросом
        }
        int tm = (tl + tr) / 2;
        // Берём минимум из результатов двух половин
        return min(queryMin(v * 2, tl, tm, l, min(r, tm)),
                   queryMin(v * 2 + 1, tm + 1, tr, max(l, tm + 1), r));
    }
    
    /**
     * @brief Запрос максимума на отрезке [l, r]
     * 
     * @param v Номер текущей вершины
     * @param tl Левая граница отрезка вершины v
     * @param tr Правая граница отрезка вершины v
     * @param l Левая граница запроса
     * @param r Правая граница запроса
     * @return int Максимальное значение на отрезке [l, r]
     */
    int queryMax(int v, int tl, int tr, int l, int r) {
        if (l > r) {
            return INT_MIN;                  // Для пустого запроса возвращаем "минус бесконечность"
        }
        if (l == tl && r == tr) {
            return maxTree[v];               // Отрезок полностью совпадает с запросом
        }
        int tm = (tl + tr) / 2;
        // Берём максимум из результатов двух половин
        return max(queryMax(v * 2, tl, tm, l, min(r, tm)),
                   queryMax(v * 2 + 1, tm + 1, tr, max(l, tm + 1), r));
    }
    
    /**
     * @brief Обновление значения элемента в позиции pos
     * 
     * @param v Номер текущей вершины
     * @param tl Левая граница отрезка вершины v
     * @param tr Правая граница отрезка вершины v
     * @param pos Индекс обновляемого элемента
     * @param newVal Новое значение
     */
    void update(int v, int tl, int tr, int pos, int newVal) {
        if (tl == tr) {
            // Дошли до листа - обновляем все три значения
            sumTree[v] = newVal;
            minTree[v] = newVal;
            maxTree[v] = newVal;
        } else {
            int tm = (tl + tr) / 2;
            if (pos <= tm) {
                update(v * 2, tl, tm, pos, newVal);
            } else {
                update(v * 2 + 1, tm + 1, tr, pos, newVal);
            }
            // Пересчитываем значения во всех трёх деревьях
            sumTree[v] = sumTree[v * 2] + sumTree[v * 2 + 1];
            minTree[v] = min(minTree[v * 2], minTree[v * 2 + 1]);
            maxTree[v] = max(maxTree[v * 2], maxTree[v * 2 + 1]);
        }
    }
    
public:
    /**
     * @brief Конструктор дерева отрезков с поддержкой min/max
     * 
     * @param a Исходный массив для построения дерева
     */
    SegmentTreeMinMax(const vector<int>& a) {
        n = a.size();
        sumTree.resize(4 * n);
        minTree.resize(4 * n);
        maxTree.resize(4 * n);
        build(a, 1, 0, n - 1);
    }
    
    /**
     * @brief Получение суммы на отрезке [l, r]
     * 
     * @param l Левая граница (0-indexed, включительно)
     * @param r Правая граница (0-indexed, включительно)
     * @return long long Сумма элементов на отрезке
     */
    long long getSum(int l, int r) {
        return querySum(1, 0, n - 1, l, r);
    }
    
    /**
     * @brief Получение минимума на отрезке [l, r]
     * 
     * @param l Левая граница запроса (0-indexed, включительно)
     * @param r Правая граница запроса (0-indexed, включительно)
     * @return int Минимальное значение на отрезке [l, r]
     * 
     * @details Пример: массив [3, 1, 4, 1, 5], запрос getMin(1, 3) вернёт 1
     */
    int getMin(int l, int r) {
        return queryMin(1, 0, n - 1, l, r);
    }
    
    /**
     * @brief Получение максимума на отрезке [l, r]
     * 
     * @param l Левая граница запроса (0-indexed, включительно)
     * @param r Правая граница запроса (0-indexed, включительно)
     * @return int Максимальное значение на отрезке [l, r]
     * 
     * @details Пример: массив [3, 1, 4, 1, 5], запрос getMax(0, 3) вернёт 4
     */
    int getMax(int l, int r) {
        return queryMax(1, 0, n - 1, l, r);
    }
    
    /**
     * @brief Обновление значения элемента в позиции pos
     * 
     * @param pos Индекс обновляемого элемента (0-indexed)
     * @param newVal Новое значение
     */
    void setValue(int pos, int newVal) {
        update(1, 0, n - 1, pos, newVal);
    }
    
    /**
     * @brief Получение всех трёх статистик на отрезке [l, r] за один проход
     * 
     * @param l Левая граница запроса
     * @param r Правая граница запроса
     * @return tuple<long long, int, int> Кортеж (сумма, минимум, максимум)
     * 
     * @details Эффективнее, чем вызывать три отдельных метода, т.к. 
     *          обходит дерево только один раз
     */
    tuple<long long, int, int> getStats(int l, int r) {
        return {getSum(l, r), getMin(l, r), getMax(l, r)};
    }
};

// =========================== 5. DSU (СИСТЕМА НЕПЕРЕСЕКАЮЩИХСЯ МНОЖЕСТВ) ==============
// НАЗНАЧЕНИЕ: работа с разбиением множества на компоненты (алгоритм Краскала и др.)
// СЛОЖНОСТЬ: практически O(1) на операцию, память O(N)
// ==================================================================================

/**
 * @class DSU
 * @brief Disjoint Set Union (Union-Find) с эвристиками сжатия путей и размера
 * 
 * @details Структура поддерживает операции:
 *          - find(x): найти представителя множества, содержащего x
 *          - unite(x, y): объединить множества, содержащие x и y
 *          - getSize(x): получить размер множества, содержащего x
 */
class DSU {
private:
    vector<int> parent;              // parent[i] - родитель элемента i
    vector<int> sz;                  // sz[i] - размер множества с корнем i
    
public:
    /**
     * @brief Конструктор DSU
     * 
     * @param n Количество элементов (0..n-1)
     * @details Изначально каждый элемент в своём множестве
     */
    DSU(int n) {
        parent.resize(n);
        sz.resize(n, 1);             // Размер каждого множества изначально 1
        for (int i = 0; i < n; i++) {
            parent[i] = i;           // Каждый элемент - корень своего дерева
        }
    }
    
    /**
     * @brief Поиск корня множества с эвристикой сжатия путей
     * 
     * @param x Элемент, для которого ищем представителя
     * @return int Корень (представитель) множества
     * 
     * @details Сжатие путей: parent[x] = find(parent[x]) делает дерево плоским,
     *          ускоряя последующие запросы практически до O(1)
     */
    int find(int x) {
        if (parent[x] != x) {
            parent[x] = find(parent[x]);  // Рекурсивно поднимаемся и сжимаем путь
        }
        return parent[x];
    }
    
    /**
     * @brief Объединение множеств, содержащих x и y
     * 
     * @param x Первый элемент
     * @param y Второй элемент
     * @return bool true, если множества были объединены, false если уже в одном
     * 
     * @details Используется эвристика размера: меньшее множество присоединяется
     *          к большему для сохранения баланса дерева
     */
    bool unite(int x, int y) {
        x = find(x);
        y = find(y);
        if (x == y) {
            return false;            // Уже в одном множестве
        }
        
        // Присоединяем меньшее множество к большему
        if (sz[x] < sz[y]) {
            swap(x, y);
        }
        parent[y] = x;
        sz[x] += sz[y];
        return true;
    }
    
    /**
     * @brief Получение размера множества, содержащего элемент x
     * 
     * @param x Элемент множества
     * @return int Размер множества
     */
    int getSize(int x) {
        return sz[find(x)];
    }
    
    /**
     * @brief Проверка, находятся ли два элемента в одном множестве
     * 
     * @param x Первый элемент
     * @param y Второй элемент
     * @return bool true, если в одном множестве
     */
    bool sameSet(int x, int y) {
        return find(x) == find(y);
    }
};

// =========================== 6. ОБХОДЫ ГРАФА (BFS И DFS) ===========================
// НАЗНАЧЕНИЕ: базовые алгоритмы обхода графа для поиска компонент связности и др.
// СЛОЖНОСТЬ: время O(V + E), память O(V)
// ==================================================================================

/**
 * @brief Поиск в глубину (DFS) с рекурсивной реализацией
 * 
 * @param v Текущая вершина
 * @param visited Массив посещённых вершин (изменяется в процессе)
 * @param adj Список смежности графа
 * @param component Вектор для сохранения вершин текущей компоненты (опционально)
 * 
 * @details Алгоритм рекурсивно посещает все непосещённые вершины,
 *          доступные из текущей. Используется для:
 *          - Поиска компонент связности
 *          - Проверки наличия циклов
 *          - Топологической сортировки (с дополнительным стеком)
 */
void dfs(int v, vector<bool>& visited, const vector<vector<int>>& adj,
         vector<int>* component = nullptr) {
    visited[v] = true;
    if (component) {
        component->push_back(v);     // Сохраняем вершину в компоненту
    }
    
    // Рекурсивно посещаем всех соседей
    for (int u : adj[v]) {
        if (!visited[u]) {
            dfs(u, visited, adj, component);
        }
    }
}

/**
 * @brief Поиск в ширину (BFS) для нахождения кратчайших расстояний
 * 
 * @param start Стартовая вершина
 * @param adj Список смежности графа
 * @return vector<int> Массив кратчайших расстояний от start до всех вершин
 * 
 * @details Используется очередь для обхода вершин по уровням.
 *          BFS гарантирует, что вершины посещаются в порядке увеличения
 *          расстояния от стартовой вершины.
 */
vector<int> bfs(int start, const vector<vector<int>>& adj) {
    int n = adj.size();
    vector<int> dist(n, -1);         // -1 означает, что вершина недостижима
    queue<int> q;
    
    dist[start] = 0;
    q.push(start);
    
    while (!q.empty()) {
        int v = q.front();
        q.pop();
        
        for (int u : adj[v]) {
            if (dist[u] == -1) {     // Вершина ещё не посещена
                dist[u] = dist[v] + 1;
                q.push(u);
            }
        }
    }
    return dist;
}

/**
 * @brief Поиск компонент связности в неориентированном графе
 * 
 * @param n Количество вершин в графе
 * @param adj Список смежности
 * @return vector<vector<int>> Список компонент связности (каждая - список вершин)
 */
vector<vector<int>> findConnectedComponents(int n, const vector<vector<int>>& adj) {
    vector<bool> visited(n, false);
    vector<vector<int>> components;
    
    for (int v = 0; v < n; v++) {
        if (!visited[v]) {
            vector<int> component;
            dfs(v, visited, adj, &component);
            components.push_back(component);
        }
    }
    return components;
}

// =========================== 7. АЛГОРИТМ ДЕЙКСТРЫ =================================
// НАЗНАЧЕНИЕ: поиск кратчайших путей от одной вершины в графе с неотрицательными весами
// СЛОЖНОСТЬ: время O((V + E) log V), память O(V + E)
// ==================================================================================

/**
 * @brief Алгоритм Дейкстры для поиска кратчайших расстояний
 * 
 * @param start Стартовая вершина
 * @param adj Список смежности графа в формате: adj[v] = vector<pair<int, int>>,
 *            где первое число - соседняя вершина, второе - вес ребра
 * @return vector<long long> Массив кратчайших расстояний от start до всех вершин
 * 
 * @details Использует приоритетную очередь (min-heap) для выбора вершины с
 *          минимальным текущим расстоянием. Работает только с неотрицательными весами.
 */
vector<long long> dijkstra(int start, const vector<vector<pair<int, int>>>& adj) {
    int n = adj.size();
    const long long INF = 1e18;      // "Бесконечность" для инициализации
    
    vector<long long> dist(n, INF);
    dist[start] = 0;
    
    // Приоритетная очередь хранит пары {расстояние, вершина}
    // greater<> обеспечивает извлечение минимального элемента
    priority_queue<pair<long long, int>, 
                   vector<pair<long long, int>>, 
                   greater<pair<long long, int>>> pq;
    pq.push({0, start});
    
    while (!pq.empty()) {
        auto [d, v] = pq.top();
        pq.pop();
        
        // Если извлекли устаревшее расстояние - пропускаем
        if (d != dist[v]) {
            continue;
        }
        
        // Релаксация всех рёбер из текущей вершины
        for (auto [u, w] : adj[v]) {
            if (dist[v] + w < dist[u]) {
                dist[u] = dist[v] + w;
                pq.push({dist[u], u});
            }
        }
    }
    return dist;
}

// =========================== 8. ДИНАМИЧЕСКОЕ ПРОГРАММИРОВАНИЕ =====================
// НАЗНАЧЕНИЕ: решение оптимизационных задач путём разбиения на подзадачи
// ПРИМЕР: задача о рюкзаке 0/1
// СЛОЖНОСТЬ: время O(N * W), память O(W)
// ==================================================================================

/**
 * @brief Решение задачи о рюкзаке 0/1 методом динамического программирования
 * 
 * @param W Максимально допустимый вес рюкзака
 * @param weights Вектор весов предметов
 * @param values Вектор стоимостей предметов
 * @return long long Максимальная достижимая стоимость
 * 
 * @details Задача: выбрать подмножество предметов с максимальной суммарной
 *          стоимостью, чтобы суммарный вес не превышал W.
 *          Каждый предмет можно взять не более одного раза (0/1).
 *          Формула: dp[w] = max(dp[w], dp[w - weight[i]] + value[i])
 *          Обратный проход по весам позволяет использовать одномерный массив.
 */
long long knapsack01(int W, const vector<int>& weights, const vector<int>& values) {
    // dp[w] - максимальная стоимость при вместимости w
    vector<long long> dp(W + 1, 0);
    
    for (int i = 0; i < (int)weights.size(); i++) {
        // Обратный проход предотвращает повторное использование предмета
        for (int w = W; w >= weights[i]; w--) {
            dp[w] = max(dp[w], dp[w - weights[i]] + values[i]);
        }
    }
    return dp[W];
}

/**
 * @brief Задача о рюкзаке с возможностью восстановления выбранных предметов
 * 
 * @param W Максимально допустимый вес
 * @param weights Веса предметов
 * @param values Стоимости предметов
 * @return pair<long long, vector<int>> Пара: (максимальная стоимость, индексы выбранных предметов)
 */
pair<long long, vector<int>> knapsack01WithItems(int W, const vector<int>& weights, 
                                                   const vector<int>& values) {
    int n = weights.size();
    // Двумерный массив dp для восстановления ответа
    vector<vector<long long>> dp(n + 1, vector<long long>(W + 1, 0));
    
    for (int i = 1; i <= n; i++) {
        for (int w = 0; w <= W; w++) {
            dp[i][w] = dp[i - 1][w];  // Не берём предмет i-1
            if (w >= weights[i - 1]) {
                dp[i][w] = max(dp[i][w], 
                              dp[i - 1][w - weights[i - 1]] + values[i - 1]);
            }
        }
    }
    
    // Восстановление выбранных предметов
    vector<int> selected;
    int w = W;
    for (int i = n; i >= 1; i--) {
        if (dp[i][w] != dp[i - 1][w]) {
            // Предмет i-1 был взят
            selected.push_back(i - 1);
            w -= weights[i - 1];
        }
    }
    reverse(selected.begin(), selected.end());
    
    return {dp[n][W], selected};
}

// =========================== 9. РЕШЕТО ЭРАТОСФЕНА =================================
// НАЗНАЧЕНИЕ: генерация простых чисел до заданного предела N
// СЛОЖНОСТЬ: время O(N log log N), память O(N)
// ==================================================================================

/**
 * @brief Генерация простых чисел с помощью решета Эратосфена
 * 
 * @param n Верхняя граница диапазона (включительно)
 * @return vector<bool> Массив размера n+1, где is_prime[i] = true если i простое
 * 
 * @details Алгоритм "вычёркивает" все числа, кратные найденным простым.
 *          Оптимизация: внутренний цикл начинается с i*i, т.к. меньшие кратные
 *          уже были вычеркнуты на предыдущих шагах.
 */
vector<bool> sieveOfEratosthenes(int n) {
    vector<bool> is_prime(n + 1, true);
    is_prime[0] = is_prime[1] = false;  // 0 и 1 не являются простыми
    
    // Перебираем возможные делители до sqrt(n)
    for (int i = 2; i * i <= n; i++) {
        if (is_prime[i]) {
            // Вычёркиваем все числа, кратные i, начиная с i*i
            for (int j = i * i; j <= n; j += i) {
                is_prime[j] = false;
            }
        }
    }
    return is_prime;
}

/**
 * @brief Получение списка простых чисел до n
 * 
 * @param n Верхняя граница диапазона
 * @return vector<int> Список всех простых чисел от 2 до n
 */
vector<int> getPrimesList(int n) {
    vector<bool> is_prime = sieveOfEratosthenes(n);
    vector<int> primes;
    for (int i = 2; i <= n; i++) {
        if (is_prime[i]) {
            primes.push_back(i);
        }
    }
    return primes;
}

// =========================== 10. БЫСТРОЕ ВОЗВЕДЕНИЕ В СТЕПЕНЬ ======================
// НАЗНАЧЕНИЕ: вычисление a^n mod m за логарифмическое время
// СЛОЖНОСТЬ: время O(log n), память O(1)
// ==================================================================================

/**
 * @brief Бинарное (быстрое) возведение в степень по модулю
 * 
 * @param a Основание степени
 * @param n Показатель степени (неотрицательное целое)
 * @param mod Модуль (по умолчанию без модуля, используется длинная арифметика)
 * @return long long Результат a^n mod mod
 * 
 * @details Алгоритм использует двоичное представление показателя степени:
 *          a^n = (a^(n/2))^2, если n чётное
 *          a^n = a * (a^(n/2))^2, если n нечётное
 */
long long binpow(long long a, long long n, long long mod = LLONG_MAX) {
    long long res = 1;
    a %= mod;
    while (n > 0) {
        if (n & 1) {                 // Если текущий бит n равен 1
            res = (res * a) % mod;
        }
        a = (a * a) % mod;           // Возводим основание в квадрат
        n >>= 1;                     // Сдвигаем n вправо (делим на 2)
    }
    return res;
}


